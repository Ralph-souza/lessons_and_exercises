# Python e uma linguagem dinamicamente tipada, ou seja, sem necessidade de declarar o tipo de dados que a variável
# receberá diferente de outras linguagens como C por exemplo, na qual você determina o tipo de dado da variável em si.
# Linguagens que recebem esse tipo de declaração são conhecidas como linguagens estaticamente ou fortemente tipadas.

# Variáveis -> São espaços de memória onde armazenamos informações que poderão ser acessíveis posteriomente através
# do nome atribuído a mesma.
a = 10
b = 5.2

print(a + b)

# Em python podemos mudar o tipo de dados a qualquer momento por exemplo, num primeiro momento podemos ter
# uma variável que receba um número inteiro, se mudarmos o valor da variável para uma string por exemplo ela seguira
# funcionando, talvez não execute a função esperada, como no exemplo abaixo.
a = "Agora sou uma string!"

print(a)

# Vale lembrar que a estrutura de uma variável é composta pelo identificador(nome), símbolo de atribuição(=) e valor.
