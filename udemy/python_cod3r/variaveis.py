# Python e uma linguagem dinamicamente tipada, ou seja, sem necessadide de declarar o tipo de dados que a variavel
# recebera diferente de outras linguagens como C por exemplo, na qual vc determina o tipo de dado da variavel em si.
# Linguagens que recebem esse tipo de declaracao sao conhecidas como linguagens estaticamente tipadas.

# Variaveis -> Sao espacos de memoria onde armazenamos informacoes que poderao ser acessiveis posteriomente atraves
# do nome atribuido a mesma.
a = 10
b = 5.2

print(a + b)

# Em python podemos mudar o tipo de dados a qualquer momento por exemplo, num primeiro momento podemos ter
# uma variavel que receba um numero inteiro, se mudarmos o valor da variavel para uma string por exemplo ela seguira
# funcionando, talvez nao execute a funcao esperada, como no exemplo abaixo.
a = "Agora sou uma string!"

print(a)

# Vale lembrar que a estrutura de uma variavel e composta pelo identificador(nome), simbolo de atribuicao(=) e valor.
